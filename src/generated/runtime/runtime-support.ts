class RigBoundedOutputCollectorClass {
  chunks = [];
  capturedBytes = 0;
  truncated = false;
  constructor(maxBytes) { this.maxBytes = maxBytes; }
  capture(buffer) {
    const remaining = Math.max(0, this.maxBytes - this.capturedBytes);
    if (remaining > 0) {
      const captured = buffer.subarray(0, remaining);
      this.chunks.push(captured);
      this.capturedBytes += captured.byteLength;
    }
    if (buffer.byteLength > remaining) this.truncated = true;
  }
  result() {
    const content = this.decodeUtf8Prefix(Buffer.concat(this.chunks, this.capturedBytes));
    return this.truncated ? content + "\\n[rig: output truncated]" : content;
  }
  decodeUtf8Prefix(buffer) {
    const strictDecoder = new TextDecoder("utf-8", { fatal: true });
    for (let removed = 0; removed <= Math.min(3, buffer.byteLength); removed++) {
      try { return strictDecoder.decode(buffer.subarray(0, buffer.byteLength - removed)); }
      catch {}
    }
    return new TextDecoder().decode(buffer);
  }
}

class RigManagedProcessClass {
  processGroup = process.platform !== "win32";
  timedOut = false;
  constructor(child, command, timeoutMs) {
    this.child = child;
    this.command = command;
    this.timeoutMs = timeoutMs;
  }
  async wait() {
    this.child.once("error", (error) => { this.spawnError = error; });
    this.timeout = setTimeout(() => this.terminate(), this.timeoutMs);
    try {
      const exitCode = await new Promise((resolve) => this.child.once("close", (code) => resolve(code ?? 1)));
      if (this.spawnError) throw new Error("Command could not start: " + this.command[0] + ": " + this.spawnError.message);
      if (this.timedOut) throw new Error("Command timed out after " + this.timeoutMs + "ms.");
      return exitCode;
    } finally {
      if (this.timeout) clearTimeout(this.timeout);
      if (this.escalation) clearTimeout(this.escalation);
    }
  }
  terminate() {
    this.timedOut = true;
    this.signal("SIGTERM");
    this.escalation = setTimeout(() => this.signal("SIGKILL"), 100);
  }
  signal(signal) {
    try {
      if (this.processGroup && this.child.pid) process.kill(-this.child.pid, signal);
      else this.child.kill(signal);
    } catch {}
  }
}

class RigShellRuntimeClass {
  async $(strings, ...values) { return this.bash(this.renderTemplateCommand(strings, values)); }
  async bash(command, options = {}) { return this.runProcess(["bash", "-lc", command], options, [command]); }
  async exec(args, options = {}) {
    if (!Array.isArray(args) || args.length === 0 || args.some((arg) => typeof arg !== "string" || arg.length === 0)) {
      throw new Error("shell.exec expects a non-empty array of command arguments.");
    }
    return this.runProcess(args, options);
  }
  async json(args, options = {}) {
    const result = await this.exec(args, options);
    if (result.exitCode !== 0) throw new Error("Command failed before JSON could be parsed.");
    return JSON.parse(result.stdout);
  }
  async runProcess(args, options = {}, reportedCommand = args) {
    const timeoutMs = options.timeoutMs ?? 30000;
    const maxOutputBytes = options.maxOutputBytes ?? 1048576;
    const child = spawn(args[0], args.slice(1), {
      cwd: options.cwd ?? process.cwd(),
      env: { ...process.env, ...options.env },
      stdio: ["ignore", "pipe", "pipe"],
      detached: process.platform !== "win32",
    });
    const stdout = new RigBoundedOutputCollectorClass(maxOutputBytes);
    const stderr = new RigBoundedOutputCollectorClass(maxOutputBytes);
    child.stdout.on("data", (chunk) => stdout.capture(chunk));
    child.stderr.on("data", (chunk) => stderr.capture(chunk));
    const exitCode = await new RigManagedProcessClass(child, reportedCommand, timeoutMs).wait();
    return { command: reportedCommand, stdout: stdout.result(), stderr: stderr.result(), exitCode };
  }
  renderTemplateCommand(strings, values) {
    return strings.reduce((command, part, index) => command + part + (index < values.length ? this.renderTemplateValue(values[index]) : ""), "");
  }
  renderTemplateValue(value) {
    if (Array.isArray(value)) return value.map((item) => this.shellQuote(String(item))).join(" ");
    return this.shellQuote(String(value));
  }
  shellQuote(value) {
    if (/^[A-Za-z0-9_./:=@%+,-]+$/.test(value)) return value;
    return "'" + value.replaceAll("'", "'\\''") + "'";
  }
}

class RigCommandRunnerRuntimeClass {
  async run(options) {
    const id = options.tool ? options.tool + "." + options.command : options.command;
    if (!/^[A-Za-z0-9_-]+[.][A-Za-z0-9_-]+$/.test(id)) throw new Error("Command id must use <tool>.<command>: " + id);
    if (options.input !== undefined && options.args?.length) throw new Error("Use input or args, not both.");

    const args = ["rig", "run", id];
    if (options.input !== undefined) args.push("--input", JSON.stringify(options.input));
    if (options.dryRun) args.push("--dry-run");
    args.push(...(options.args ?? []));

    const result = await new RigShellRuntimeClass().exec(args);
    if (result.exitCode !== 0) throw new Error("Rig tool command failed: " + id + "\\n" + result.stderr);

    const envelope = JSON.parse(result.stdout);
    if (envelope.errors?.length) throw new Error("Rig tool command failed: " + id + "\\n" + JSON.stringify(envelope.errors, null, 2));
    return envelope.data;
  }
}

