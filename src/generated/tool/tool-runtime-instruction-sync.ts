import { readFile, writeFile } from "node:fs/promises";
import type { ConfigOptions } from "../../config/config";
import { ToolDiscoveryServiceClass } from "../../registry/discover";
import { CurrentRigToolApiVersion } from "../../tools/domain/tool-api";

type BunFileApi = {
  file(path: string): { text(): Promise<string> };
  write(path: string, content: string): Promise<number>;
};

export type ToolRuntimeInstructionUpdate = {
  name: string;
  path: string;
  changed: boolean;
};

export type ToolRuntimeInstructionSyncResult = {
  tools: ToolRuntimeInstructionUpdate[];
};

const StartMarker = "// rig:runtime-reference:start";
const EndMarker = "// rig:runtime-reference:end";
const ToolApiVersionMarker = "// rig:tool-api-version";

const RuntimeReference = `Generated by Rig. This block is refreshed automatically.

Rig tool runtime:
- Tool API v2 uses a direct factory export: \`export default (rig: RigToolKit) => rig.defineTool({ ... });\`.
- Rig derives the tool name from its folder. The \`name\` property remains supported for v1 tools.
- \`rig.z\` is Zod. Docs: https://zod.dev/
- \`rig.defineTool(definition)\` defines the tool.
- Define \`commands\` as \`(command) => ({ ... })\`, then define each command with the bound \`command({ description, input, output, run })\` helper. \`rig.defineCommand\` remains supported for v1 tools.
- Use \`rig.z.object({ ... })\` for input/output schemas.
- Tools run under Bun with fallback auto-install enabled, so tool files can import npm packages; add explicit package versions when reproducibility matters. Example: \`import sharp from "sharp@0.34.4";\`.
- \`rig.run({ command: "tool.command", input })\` runs another registered Rig command and returns its data.
- \`rig.$\` runs a Bun Shell tagged template command with escaped interpolations. Docs: https://bun.com/docs/runtime/shell
- \`rig.args()\` builds argv arrays with \`.raw()\`, \`.flag()\`, \`.value()\`, \`.values()\`, and \`.toArray()\`.
- \`rig.paths.home()\`, \`rig.paths.resolve(cwd, path)\`, \`rig.paths.ensureParent(path)\`, and \`rig.paths.size(path)\` cover common path work.
- Add \`env: rig.z.object({ KEY: rig.z.string() })\` to validate local secrets or settings and receive parsed values on \`context.env\`.
- Run \`rig env <tool> KEY=VALUE\` to write the tool \`.env\` file; run \`rig env <tool> remove KEY\` to remove a value.
- To add another registry for tool files, run \`rig registry create [path]\` (defaults to current directory).

Tool logging:
- Use \`context.log.info(...)\`, \`context.log.warn(...)\`, or another Pino-style method for structured logs with a default \`tool:<tool>.<command>\` prefix.
- Rig writes app and tool logs to \`~/rig/.logs\`, rolls files by size, and keeps logs for seven days by default.

Tool key-value state:
- Use \`context.kv.set(key, value)\` and \`context.kv.get(key)\` for lightweight JSON state stored beside the tool entry file as \`kv.sqlite\`.

Tool query cache:
- Use \`await context.cache.query({ queryKey, queryFn, staleTime })\` for JSON-compatible derived data stored beside the tool entry file as \`cache.sqlite\`.
- Query keys are non-empty JSON-compatible arrays. Missing and stale data wait for \`queryFn\` and return its refreshed value; fresh data returns immediately.
- Use \`context.cache.peek(queryKey)\`, \`.set(queryKey, value)\`, \`.invalidate(queryKey)\`, \`.remove(queryKey)\`, and \`.clear()\` for explicit cache control.

Tool SQLite database:
- Add \`setupDb: (db) => { db.migrate(1, "create table", "create table ..."); }\` when a tool needs persistent state.
- Rig stores the database beside the tool entry file as \`index.sqlite\` and runs \`setupDb\` before every command.
- \`db\` is Bun SQLite with \`strict: true\`, WAL mode, and an extra \`db.migrate(version, name, sql)\` helper. Docs: https://bun.com/docs/runtime/sqlite

Tool collections:
- Add \`collections: { name: { schema: rig.z.object({ ... }) } }\` to the tool definition to declare document stores.
- Each collection is a directory of \`.md\` files (beside the tool entry file) with validated YAML frontmatter and a markdown body.
- Access via \`context.collections.<name>\` in command run functions.
- API: \`.create({ id, data, body })\`, \`.getEntry(id)\`, \`.update(id, { data, body })\`, \`.upsert({ id, data, body })\`, \`.remove(id)\`
- Query: \`.list({ where, sort, limit })\`, \`.search(query, { limit })\`, \`.count(where)\`, \`.getCollection(filterFn)\`, \`.clear()\`
- A hidden \`.index.sqlite\` with FTS5 is auto-managed for instant full-text search and structured queries on frontmatter fields.
- Files on disk are the source of truth; hand-edits are reconciled on next access.
- Optional \`generateId: (data) => string\` derives the filename from entry data; otherwise pass \`id\` explicitly.
- Schema-less collections (no \`schema\` key) are allowed; FTS still works, no validation on write.

Command run context:
- \`context.input\` is already validated by the input schema.
- \`context.env\` is already validated by the tool env schema when defined.
- \`context.processEnv\` is the process environment.
- \`context.cwd\` is the current working directory.
- \`context.db\` is available when the tool defines \`setupDb\`.
- \`context.kv\` is always available for lightweight JSON key-value state.
- \`context.cache\` is always available for persistent query data with explicit foreground refreshes.
- \`context.log\` is a structured Pino logger for this tool command.
- \`context.rig\` is the toolkit (same as the factory \`rig\` arg, useful for nested tool calls).
- \`context.collections\` provides handles for each declared collection (empty object if none defined).
- Return a value that matches the output schema.
`;

export class ToolRuntimeInstructionSyncServiceClass {
  private readonly discovery: ToolDiscoveryServiceClass;

  constructor(options: ConfigOptions = {}) {
    this.discovery = new ToolDiscoveryServiceClass(options);
  }

  async sync(): Promise<ToolRuntimeInstructionSyncResult> {
    const tools = await this.discovery.discover();
    const updates = await Promise.all(
      tools.map(async (tool) => ({
        name: tool.name,
        path: tool.toolPath,
        changed: await this.updateFile(tool.toolPath),
      })),
    );
    return { tools: updates };
  }

  renderPrefix(version = CurrentRigToolApiVersion): string {
    return [
      StartMarker,
      `${ToolApiVersionMarker} ${version}`,
      ...RuntimeReference.trimEnd()
        .split("\n")
        .map((line) => (line ? `// ${line}` : "//")),
      EndMarker,
    ].join("\n");
  }

  upsertPrefix(source: string): string {
    const version = this.sourceVersion(source);
    const withoutExisting = source
      .replace(this.managedBlockPattern(), "")
      .replace(/^\/\/ rig:tool-api-version \d+\s*$/gm, "")
      .trimStart();
    const shebang = withoutExisting.match(/^(#!.*\n)/);
    const prefix = this.renderPrefix(version);
    const body = shebang ? withoutExisting.slice(shebang[0].length).trimStart() : withoutExisting;
    const next = [prefix, body.trimEnd()].filter(Boolean).join("\n\n");
    return `${shebang?.[0] ?? ""}${next}\n`;
  }

  private async updateFile(path: string): Promise<boolean> {
    const existing = await this.readText(path);
    const next = this.upsertPrefix(existing);
    if (next === existing) return false;
    await this.writeText(path, next);
    return true;
  }

  private async readText(path: string): Promise<string> {
    const bun = this.bunFileApi();
    /* v8 ignore next */
    if (bun) return bun.file(path).text();
    return readFile(path, "utf8");
  }

  private async writeText(path: string, content: string): Promise<void> {
    const bun = this.bunFileApi();
    /* v8 ignore next 4 */
    if (bun) {
      await bun.write(path, content);
      return;
    }
    await writeFile(path, content, "utf8");
  }

  private bunFileApi(): BunFileApi | undefined {
    const candidate = (globalThis as typeof globalThis & { Bun?: Partial<BunFileApi> }).Bun;
    /* v8 ignore next 3 */
    if (typeof candidate?.file === "function" && typeof candidate.write === "function") {
      return candidate as BunFileApi;
    }
    return undefined;
  }

  private managedBlockPattern(): RegExp {
    return new RegExp(
      `${this.escapeRegExp(StartMarker)}[\\s\\S]*?${this.escapeRegExp(EndMarker)}\\n*`,
    );
  }

  private sourceVersion(source: string): number {
    const match = /^\/\/ rig:tool-api-version (\d+)\s*$/m.exec(source);
    if (!match) return 1;
    const version = Number.parseInt(match[1]!, 10);
    return Number.isSafeInteger(version) && version > 0 ? version : 1;
  }

  private escapeRegExp(value: string): string {
    return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
  }
}
